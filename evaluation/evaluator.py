"""
evaluator.py
------------
Evaluation framework for generated test cases.

Metrics computed per generated test file:
  - Parse Rate  (PR%)  : Can ast.parse() read it without SyntaxError?
  - Execution Rate (EX%): Does pytest run without collection errors?
  - Line Coverage (CV%) : What % of the source file is covered by the tests?
  - Combined Coverage   : Coverage across ALL test files together (more meaningful)

Usage:
    from evaluation.evaluator import evaluate_file, evaluate_all

    # Evaluate a single generated test file against its source
    result = evaluate_file("outputs/test_add.py", source_file="data/sample_code.py")

    # Evaluate every .py in the outputs/ folder
    results = evaluate_all(output_dir="outputs", source_file="data/sample_code.py")
"""

from __future__ import annotations

import ast
import os
import subprocess
import sys
import shutil
from typing import Any, Dict, List, Optional


# ── Public API ─────────────────────────────────────────────────────────────────

def evaluate_file(
    test_file: str,
    source_file: Optional[str] = None,
) -> Dict[str, Any]:
    """
    Evaluate a single generated test file.

    Args:
        test_file   : Path to the generated .py test file.
        source_file : Path to the original source .py file (needed for coverage).
                      If None, coverage metric is skipped.

    Returns:
        {
            "test_file"          : str,
            "parse_rate"         : float,   # 1.0 or 0.0
            "exec_rate"          : float,   # 1.0 or 0.0
            "coverage"           : float,   # 0.0-100.0 per-file (or None)
            "combined_coverage"  : float,   # 0.0-100.0 across all files (or None)
            "parse_error"        : str,
            "exec_error"         : str,
            "status"             : str,     # "ok" | "parse_error" | "exec_error"
        }
    """
    result: Dict[str, Any] = {
        "test_file"         : test_file,
        "parse_rate"        : 0.0,
        "exec_rate"         : 0.0,
        "coverage"          : None,
        "combined_coverage" : None,
        "parse_error"       : None,
        "exec_error"        : None,
        "status"            : "ok",
    }

    # ── Step 1: Parse Rate ─────────────────────────────────────────────────────
    pr = _check_parse_rate(test_file)
    result["parse_rate"]  = pr["rate"]
    result["parse_error"] = pr["error"]

    if pr["rate"] == 0.0:
        result["status"] = "parse_error"
        return result

    # ── Step 2: Execution Rate ─────────────────────────────────────────────────
    test_dir = os.path.dirname(os.path.abspath(test_file))
    ex = _check_exec_rate(test_file, cwd=test_dir)
    result["exec_rate"]  = ex["rate"]
    result["exec_error"] = ex["error"]

    if ex["rate"] == 0.0:
        result["status"] = "exec_error"

    # ── Step 3: Per-file Coverage ──────────────────────────────────────────────
    if source_file and os.path.exists(source_file):
        cv = _check_coverage(test_file, source_file)
        result["coverage"] = cv["coverage_pct"]

    return result


def evaluate_all(
    output_dir: str = "outputs",
    source_file: Optional[str] = None,
) -> List[Dict[str, Any]]:
    """
    Evaluate every .py test file found in output_dir.
    Also computes COMBINED coverage across all test files together.

    Args:
        output_dir  : Folder containing generated test .py files.
        source_file : Original source file for coverage measurement.

    Returns:
        List of result dicts. Each dict also contains 'combined_coverage'
        which is the same value for all files — the total coverage when
        all tests run together.
    """
    if not os.path.exists(output_dir):
        print(f"[Evaluator] Output directory not found: '{output_dir}'")
        return []

    # ── Ensure source file is importable from output_dir ──────────────────────
    if source_file and os.path.exists(source_file):
        src_basename = os.path.basename(source_file)
        dst_path     = os.path.join(output_dir, src_basename)
        if not os.path.exists(dst_path):
            shutil.copy(source_file, dst_path)
            print(f"[Evaluator] 📋 Copied source → {dst_path}")

    # ── Ensure conftest.py exists in output_dir ────────────────────────────────
    conftest_path  = os.path.join(output_dir, "conftest.py")
    abs_output_dir = os.path.abspath(output_dir)
    conftest_content = (
        "# Auto-generated by evaluator.py\n"
        "import sys, os\n"
        f"sys.path.insert(0, r'{abs_output_dir}')\n"
    )
    with open(conftest_path, "w", encoding="utf-8") as _cf:
        _cf.write(conftest_content)
    print(f"[Evaluator] 📋 conftest.py written → {conftest_path}")

    # ── Find test files ────────────────────────────────────────────────────────
    test_files = [
        os.path.join(output_dir, f)
        for f in os.listdir(output_dir)
        if f.endswith(".py") and f.startswith("test_")
    ]

    if not test_files:
        print(f"[Evaluator] No test_*.py files found in '{output_dir}'")
        return []

    print(f"[Evaluator] Found {len(test_files)} test file(s) to evaluate...")

    # ── Per-file evaluation ────────────────────────────────────────────────────
    results = []
    for tf in sorted(test_files):
        print(f"[Evaluator]   Evaluating: {os.path.basename(tf)}")
        r = evaluate_file(tf, source_file=source_file)
        results.append(r)
        _print_result(r)

    # ── Combined coverage across ALL test files ────────────────────────────────
    combined_cv = None
    if source_file and os.path.exists(source_file):
        print(f"\n[Evaluator] 📊 Computing combined coverage across all test files...")
        combined_cv = _check_combined_coverage(output_dir, source_file)
        if combined_cv is not None:
            print(f"[Evaluator] ✅ Combined Coverage = {combined_cv:.1f}%")
        else:
            print(f"[Evaluator] ⚠️  Could not compute combined coverage")

    # Attach combined_coverage to every result so the UI can display it
    for r in results:
        r["combined_coverage"] = combined_cv

    return results


# ── Combined coverage ──────────────────────────────────────────────────────────

def _check_combined_coverage(
    output_dir: str,
    source_file: str,
) -> Optional[float]:
    """
    Run ALL test files in output_dir together with pytest --cov.
    This gives the true combined line coverage of the full test suite.
    """
    source_name = os.path.splitext(os.path.basename(source_file))[0]
    abs_out     = os.path.abspath(output_dir)

    env = os.environ.copy()
    existing_pp = env.get("PYTHONPATH", "")
    env["PYTHONPATH"] = abs_out + (os.pathsep + existing_pp if existing_pp else "")

    try:
        proc = subprocess.run(
            [
                sys.executable, "-m", "pytest",
                output_dir,
                f"--cov={source_name}",
                "--cov-report=term-missing",
                "--tb=no",
                "-q",
                "--import-mode=prepend",
            ],
            capture_output=True,
            text=True,
            timeout=120,
            cwd=abs_out,
            env=env,
        )

        output = proc.stdout + proc.stderr
        return _parse_coverage_output(output, source_name)

    except subprocess.TimeoutExpired:
        print("[Evaluator] ⚠️  Combined coverage timed out")
        return None
    except Exception as e:
        print(f"[Evaluator] ⚠️  Combined coverage error: {e}")
        return None


# ── Metric helpers ─────────────────────────────────────────────────────────────

def _check_parse_rate(test_file: str) -> Dict[str, Any]:
    """Run ast.parse() on the test file."""
    try:
        with open(test_file, "r", encoding="utf-8") as f:
            source = f.read()

        source = _strip_traceability_comments(source)
        ast.parse(source)
        return {"rate": 1.0, "error": None}

    except SyntaxError as e:
        return {
            "rate" : 0.0,
            "error": f"SyntaxError at line {e.lineno}: {e.msg}",
        }
    except Exception as e:
        return {"rate": 0.0, "error": str(e)}


def _check_exec_rate(test_file: str, cwd: Optional[str] = None) -> Dict[str, Any]:
    """Run pytest --collect-only on the test file."""
    run_cwd = cwd or os.path.dirname(os.path.abspath(test_file))

    env = os.environ.copy()
    existing_pp = env.get("PYTHONPATH", "")
    env["PYTHONPATH"] = run_cwd + (os.pathsep + existing_pp if existing_pp else "")

    try:
        proc = subprocess.run(
            [
                sys.executable, "-m", "pytest",
                test_file,
                "--collect-only",
                "--tb=short",
                "-q",
                "--import-mode=prepend",
            ],
            capture_output=True,
            text=True,
            timeout=120,
            cwd=run_cwd,
            env=env,
        )

        if proc.returncode in (0, 1, 5):
            return {"rate": 1.0, "error": None}
        else:
            return {
                "rate" : 0.0,
                "error": (proc.stderr or proc.stdout)[:500],
            }

    except subprocess.TimeoutExpired:
        return {"rate": 0.0, "error": "pytest timed out after 120s"}
    except Exception as e:
        return {"rate": 0.0, "error": str(e)}


def _check_coverage(test_file: str, source_file: str) -> Dict[str, Any]:
    """Run pytest --cov on a single test file."""
    source_name = os.path.splitext(os.path.basename(source_file))[0]
    test_dir    = os.path.dirname(os.path.abspath(test_file))

    src_in_test_dir = os.path.join(test_dir, os.path.basename(source_file))
    if not os.path.exists(src_in_test_dir) and os.path.exists(source_file):
        shutil.copy(source_file, src_in_test_dir)

    env = os.environ.copy()
    existing_pp = env.get("PYTHONPATH", "")
    env["PYTHONPATH"] = test_dir + (os.pathsep + existing_pp if existing_pp else "")

    try:
        proc = subprocess.run(
            [
                sys.executable, "-m", "pytest",
                test_file,
                f"--cov={source_name}",
                "--cov-report=term-missing",
                "--tb=no",
                "-q",
                "--import-mode=prepend",
            ],
            capture_output=True,
            text=True,
            timeout=60,
            cwd=test_dir,
            env=env,
        )

        output = proc.stdout + proc.stderr
        coverage_pct = _parse_coverage_output(output, source_name)
        return {"coverage_pct": coverage_pct}

    except subprocess.TimeoutExpired:
        print(f"[Evaluator] ⚠️  Coverage timed out for {os.path.basename(test_file)}")
        return {"coverage_pct": None}
    except Exception as e:
        print(f"[Evaluator] ⚠️  Coverage error: {e}")
        return {"coverage_pct": None}


def _parse_coverage_output(output: str, module_name: str) -> Optional[float]:
    """Parse pytest-cov terminal output to extract the coverage percentage."""
    import re

    pattern = rf"{re.escape(module_name)}\s+\d+\s+\d+\s+(\d+)%"
    match   = re.search(pattern, output)
    if match:
        return float(match.group(1))

    total_match = re.search(r"TOTAL\s+\d+\s+\d+\s+(\d+)%", output)
    if total_match:
        return float(total_match.group(1))

    return None


# ── Utility helpers ────────────────────────────────────────────────────────────

def _strip_traceability_comments(source: str) -> str:
    """Remove the traceability comment block added by test_formatter.py."""
    lines  = source.splitlines()
    output = []
    in_trace_block = False

    for line in lines:
        stripped = line.strip()
        if stripped.startswith("# ") and "=" * 20 in stripped:
            in_trace_block = not in_trace_block
            continue
        if in_trace_block and stripped.startswith("#"):
            continue
        in_trace_block = False
        output.append(line)

    return "\n".join(output)


def _print_result(result: Dict[str, Any]) -> None:
    """Pretty-print a single evaluation result."""
    fname = os.path.basename(result["test_file"])
    pr    = "✅" if result["parse_rate"] == 1.0 else "❌"
    ex    = "✅" if result["exec_rate"]  == 1.0 else "❌"
    cv    = f"{result['coverage']:.1f}%" if result["coverage"] is not None else "N/A"

    print(f"           PR={pr}  EX={ex}  CV={cv}  [{fname}]")
    if result["parse_error"]:
        print(f"             Parse error : {result['parse_error']}")
    if result["exec_error"]:
        print(f"             Exec error  : {result['exec_error'][:120]}")


# ── CLI entry point ────────────────────────────────────────────────────────────

if __name__ == "__main__":
    output_dir  = sys.argv[1] if len(sys.argv) > 1 else "outputs"
    source_file = sys.argv[2] if len(sys.argv) > 2 else None

    print(f"[Evaluator] Evaluating files in '{output_dir}'")
    if source_file:
        print(f"[Evaluator] Source file for coverage: '{source_file}'")

    results = evaluate_all(output_dir=output_dir, source_file=source_file)

    if results:
        n      = len(results)
        avg_pr = sum(r["parse_rate"] for r in results) / n * 100
        avg_ex = sum(r["exec_rate"]  for r in results) / n * 100
        combined_cv = results[0].get("combined_coverage") if results else None

        print("\n=== AGGREGATE METRICS ===")
        print(f"  Files evaluated   : {n}")
        print(f"  Parse Rate (PR)   : {avg_pr:.1f}%")
        print(f"  Exec Rate  (EX)   : {avg_ex:.1f}%")
        print(f"  Combined Coverage : {f'{combined_cv:.1f}%' if combined_cv is not None else 'N/A'}")